package vo;

import java.util.List;

public class ProdVO {
	private int lprod_code;
	private int prod_price;
	private String prod_name;
	private int prod_code;
	private String prod_cont;
	private List<ProdAttVO> prod_atts;
	
	public int getLprod_code() {
		return lprod_code;
	}
	public void setLprod_code(int lprod_code) {
		this.lprod_code = lprod_code;
	}
	public int getProd_price() {
		return prod_price;
	}
	public void setProd_price(int prod_price) {
		this.prod_price = prod_price;
	}
	public String getProd_name() {
		return prod_name;
	}
	public void setProd_name(String prod_name) {
		this.prod_name = prod_name;
	}
	public int getProd_code() {
		return prod_code;
	}
	public void setProd_code(int prod_code) {
		this.prod_code = prod_code;
	}
	public String getProd_cont() {
		return prod_cont;
	}
	public void setProd_cont(String prod_cont) {
		this.prod_cont = prod_cont;
	}
	public void setProd_atts(List<ProdAttVO> prod_atts) {
		this.prod_atts = prod_atts;
	}
	public List<ProdAttVO> getProd_atts() {
		return prod_atts;
	}
	
}
